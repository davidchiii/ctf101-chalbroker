import os

def check_utils():
    def is_tool(name):
        from shutil import which

        # print(which(name))
        return which(name) is not None
    print("рҹ”Һ Checking for required system utils....")
    req = ["docker", "curl"]
    try:
        for i in req:
            print(f"Checking {i}")
            if not is_tool(i):
                raise Exception(f"{i} not found.")
    except Exception as e:
        print(f"Error: {e}")

def check_dockerfiles():
    res = []
    for path, dirs, files in os.walk("challenges"):
        for file in files:
            if file == "Dockerfile":
                full_path = os.path.join(path, file).removesuffix("Dockerfile")
                rel_path = os.path.relpath(full_path, "challenges")
                folder = os.path.relpath(path, "challenges")
                res.append((full_path.removesuffix("Dockerfile"), folder))
    print(res)
    return res

def run_build(path, chal_name):
    import subprocess
    subprocess.run(["docker", "build", "-t", f"{chal_name}", f"{path}"])
    print(f"Building {chal_name}")


